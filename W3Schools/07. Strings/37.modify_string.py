s = "Hello, World!"

# uppercase
print(s.upper())

# lowercase
print(s.lower())

# remove whitespaces
s1 = "         Hello, World!        "
# The strip() method removes any whitespace from the beginning or the end
print(s1, "after string")
print(s1.strip())


# replace string
print(s.replace("H", "J"))

# split string
print(s.split(","))
